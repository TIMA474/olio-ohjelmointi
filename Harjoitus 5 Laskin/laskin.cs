namespace Harjoitus_5_Laskin
{
    static class laskin
    {
      //laskin muutjuat jolla laskin tulee toimii 
        public static float Summa(float a, float b)
        {
            return a + b;
        
        }

        public static float Erotus(float a, float b)
        {
            return a - b;

        }

        public static float Tulo(float a, float b)
        {
            return a * b;

        }

        public static float jako(float a, float b)
        {
            return a / b;

        }





    }


    


}
